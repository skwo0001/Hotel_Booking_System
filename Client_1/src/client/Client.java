    private void checkAvailability(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkAvailability
        
        final int NUM_FIELDS = 2 ;
        int numCorrectFields = 0;
        String errorMessage = "";
        Date chkin = jXDatePicker3.getDate();
        String checkin = "";
        Date chkout = jXDatePicker4.getDate();
        String checkout = "";
       
        if (jXDatePicker3.getDate() == null)
        {
            errorMessage = errorMessage.concat("Check in date must be chose! \n");
        } else  
        {
            // checkin date is not empty
            numCorrectFields++;
            checkin = formater.format(chkin);
        }
        
        if (jXDatePicker4.getDate() == null)
        {
            errorMessage = errorMessage.concat("Check out date must be chose! \n");
        } else  
        {
            // checkout date is not empty
            numCorrectFields++;
            checkout = formater.format(chkout);
        }
        
        
         if (numCorrectFields < NUM_FIELDS)
        {
            JOptionPane.showMessageDialog(null,errorMessage,"Incomplete/Invalid Data Entered!",JOptionPane.ERROR_MESSAGE);
        }
        else 
        {   output.println("CheckAvailability");
            System.out.println("CheckAvailability");
            String city = jComboBox1.getSelectedItem().toString();
            output.println(city);
            String hotel = jComboBox2.getSelectedItem().toString().split(" ")[0];
            output.println(hotel); 
            output.println(checkin);
            output.println(checkout);

            String availableresult = input.nextLine();
            String vacancyresult = input.nextLine();
            
            if (availableresult.equals("Success"))
                {
                    if (vacancyresult.equals("Success"))
                    {
                        JOptionPane.showMessageDialog(null, "It is available", "Check Availability", JOptionPane.INFORMATION_MESSAGE );
                        jButton8.setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(null, "Sorry, Date Invalid!", "Check Availability", JOptionPane.INFORMATION_MESSAGE);
                    }
            }
            else {
                JOptionPane.showMessageDialog(null, "Sorry, Booking is full!", "Check Availability", JOptionPane.INFORMATION_MESSAGE);
             }
        }
        
        
    }//GEN-LAST:event_checkAvailability


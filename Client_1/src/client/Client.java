    private void confirmedBooking(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmedBooking
        // TODO add your handling code here:
        final int NUM_FIELDS = 4;
        int numCorrectFields = 0;
        String errorMessage = "";
        String Name = "";
        int Phone = 0;
        String Email = "";
        String CC = "";

        //check name entered
        if (jTextField3.getText().isEmpty()){
            errorMessage = errorMessage.concat("Name field should be filled in! \n");
        } else {
            // name is not empty
            numCorrectFields++;
            Name = jTextField3.getText();
        }

        //check phone entered
        if (jTextField4.getText().isEmpty()){
            errorMessage = errorMessage.concat("Phone field should be filled in! \n");
        }else {
            try {
                if (jTextField4.getText().length() != 10)
                {
                    errorMessage = errorMessage.concat("Not a valid phone number! \n");
                } else {
                    Phone = Integer.parseInt(jTextField4.getText());
                    numCorrectFields++;
                }
            } catch(NumberFormatException e){
                errorMessage = errorMessage.concat("Invalid input for Phone number!\n");
            }
        }

        //check email inputed
        if (jTextField5.getText().isEmpty())
        {
            errorMessage = errorMessage.concat("Email field should be filled in! \n");
        }else if (!jTextField5.getText().contains("@")){
            errorMessage = errorMessage.concat("Invalid Email Address! \n");
        }
        // name is not empty
        else
        {
            numCorrectFields++;
            Email = jTextField5.getText();
        }

        //check credit card number
        if (jTextField7.getText().isEmpty())
        {
            errorMessage = errorMessage.concat("Credit Card field should be filled in! \n");
        }else if (!jTextField7.getText().matches("\\d\\d\\d\\d-\\d\\d\\d\\d-\\d\\d\\d\\d-\\d\\d\\d\\d")){
            errorMessage = errorMessage.concat("Invalid Credit Card Number ! \n");
        }else {
            // name is not empty
            numCorrectFields++;
            CC = jTextField7.getText();
        }

        if (numCorrectFields < NUM_FIELDS)
        {
            JOptionPane.showMessageDialog(null,errorMessage,"Incomplete/Invalid Data Entered!",JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            String city = jComboBox1.getSelectedItem().toString();
            String hotel = jComboBox2.getSelectedItem().toString().split(" ")[0];
            String checkin = formater.format(jXDatePicker3.getDate());
            String checkout = formater.format(jXDatePicker4.getDate());
            output.println("makeBooking");
            System.out.println("makeBooking");
            output.println(hotel);
            output.println(city);
            output.println(Name);
            output.println(Email);
            output.println(Phone);
            output.println(CC);
            output.println(checkin);
            output.println(checkout);

            String result = input.nextLine();

            if (result.equals("F"))
            {
                JOptionPane.showMessageDialog(null, "I am sorry. The Booking is now unavaliable! ", "Request Fail!", JOptionPane.ERROR_MESSAGE );
                
            } else {
                String booking_ref = input.nextLine();
                JOptionPane.showMessageDialog(null, "Your Booking is confirmed. Reference no. is "+booking_ref+".", "Booking confirmed", JOptionPane.INFORMATION_MESSAGE );
            } 

             jPanel1.setVisible(false);
                jPanel2.setVisible(false);
                jPanel3.setVisible(false);
                jButton8.setVisible(false);
                jButton2.setVisible(false);
                jButton9.setVisible(false);
            clearAll();
            
        }
        
        
    }//GEN-LAST:event_confirmedBooking

